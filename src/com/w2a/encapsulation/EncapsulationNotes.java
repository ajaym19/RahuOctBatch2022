package com.w2a.encapsulation;

public class EncapsulationNotes {

	
	/*
	 * Encapsulation:
	 * is to hide the data
	 * wrapping of data and method together
	 * access to the data is given by methods
	 * 
	 * is also called as Getters and Setters
	 * 
	 * Access Modifiers
	 * private: can be accessed only within the same class
	 * default: can be accessed only within the package, outside the package - not possible
	 * protected: can be accessed within the package, outside the package - only by Child class
	 * public: can be accessed throughout the project
	 * 
	 * Non Access Modifiers
	 * static, final 
	 * 
	 * 
	 */
	
	
}
