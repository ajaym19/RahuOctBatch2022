package com.w2a.classesobjects;

public class ClassesObjectsNotes {

	/*
	 * Object:
	 * everything we see is an object
	 * 
	 * Pen, Pencil, Bike, Car, Mobile
	 * 
	 * Objects:
	 * 1. Properties:
	 * 		attributes or ways to identify or define that object
	 * 		these are defined as varaibles
	 * 
	 * 2. Behavior: 
	 * 		what can we do using this object or what actions can be perfromed
	 * 		these are defined as Methods
	 * 
	 * Pen:
	 * Properties: color, type, shape, size, height, weight, company
	 * Behavior: write, scribble
	 * 
	 * Car:
	 * Properties: color, weight, height, brand, type, weight, price, model, make, company
	 * Behavior: drive, applyBrakes, accelerate, reverse, honk, 
	 * 
	 * Human Being:
	 * Properties: color, gender, height, weigth, aadahrcar, pan card, ban details, address, pohone number, name
	 * Behavior: walking, running, dancing, speak, eating, sleeping
	 * 15 CR
	 * 
	 * 
	 * Class:
	 * collection of similar objects
	 * blueprint
	 * 
	 * 
	 * Types of Variables
	 * 1. Static
	 * 		any variable whose value is not dependent on the object, should be made as static
	 * 		only one copy is maintained across all the objects
	 * 
	 * 2. Instance
	 * 		any variable whose value is dependent on the object, should be made as instance
	 *		separate copy is maintained for each object
	 * 
	 * 
	 * Types of Methods:
	 * 1. Static: 
	 * 		when the method is not dependent on the object, make it static
	 * 		we cannot access instance variable and methods
	 * 		we can access static variable
	 * 
	 * 2. Instance
	 * 		when the method is dependent on the object, make it instance 
	 * 		we can access instance variable
	 * 		we can access static variable
	 * 
	 */
}
