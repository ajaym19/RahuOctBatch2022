package com.w2a.abstraction;

public class AbstractionNotes {

	/*
	 * Abstraction
	 * Abstract: idea
	 * 
	 * Ajay Mishra Bank
	 * 	
	 * 
	 * we are trying to hide the code
	 * 
	 * Abstract Method: a method with no implementation
	 * 
	 * Can be achived in 2 ways:
	 * 1. Interface:
	 * 		it is the responsibility of the class implementing the Interface, to implement all the methods of that Interface
	 * 		percentage of abstraction achieved: 100%	
	 * 		all the methods are abstract by default
	 * 
	 * 2. Abstract Class:
	 * 		it is the responsibility of the child class to implement the abstract methods
	 * 		percentage of abstraction achieved: 0 to 100%
	 * 		we need to use abstract keyword for an abstract method
	 * 
	 * we can achieve multiple inheritance using Interface
	 */
}
